public class main {
    public static void main (String[] args) {

        // THIS IS A COMMENT, USE TWO 'SLASHES'
        // USED TO EXPLAIN A SECTION OF CODE

        // System.out.println("Hello, world!"); DISABLE LINES OF CODE
        System.out.println("My name is Santiago.");

        /*

        MULTI-LINE COMMENT
        EVERYTHING INSIDE
        IS A COMMENT

         */
        System.out.println("This is line 1.\nThis is line 2.");
        System.out.println("Back slash \\");
        System.out.println("0\t1\t2\t3\t4");
        System.out.println("I read the book \"The Outsiders\" ");

        //  \n   NEW LINE
        //  \t   TAB
        //  \\   BACKSLASH
        //  \"   DOUBLE QUOTE

    }
}
